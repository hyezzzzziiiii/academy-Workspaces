package days01;

public class SecondJava {

	public static void main(String[] args) {
        // 큰따옴표 안에서 줄바꿈의 동작을 시킬 수 있는 이스케이프 문자 : "\n"
		System.out.printf("1\t 겁나 쉬운 자바\n");
		System.out.printf("12\t 겁나 쉬운 자바\n");
		System.out.printf("123\t 겁나 쉬운 자바\n");
		// \t : Tab의 역할 -> 탭은 일정 글자가 채워 지지 않고 사용하면 같은 위치로 커서를 이동시킵니다.
	}

}
